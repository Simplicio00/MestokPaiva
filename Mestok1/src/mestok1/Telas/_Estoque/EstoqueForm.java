    public void PreencheTabelaComEstoqueDeProduto(String categoriaNome, String produtoNome){
        Produto produto = rep_produto.GetProdutoPorNome(produtoNome, 
                rep_categoria.GetCategoriaPorNome(categoriaNome, SistemaRepository.sistemaId, Boolean.TRUE).getCagegoriaId());
        
        DefaultTableModel model = (DefaultTableModel) tb_estoque.getModel();
        model.setRowCount(0);
        
        if (produto != null) {
            ArrayList<Estoque> estoque_lista = rep_estoque.GetEstoquesDoProduto(produto.getProdutoId());
            
            if (estoque_lista.isEmpty()) {
                return;
            }
            
            int comprimento_lista = estoque_lista.size();
            

            
            for (int i = 0; i <= (comprimento_lista-1); i++) {
                int id = estoque_lista.get(i).getId();
                String nome = estoque_lista.get(i).getNome();
                float valor = estoque_lista.get(i).getValorContabil();
                model.addRow(new Object[] { id, nome, valor });
            }
        }
    }
    
    public void AtualizaCheckBoxPorCategoria(){
        checkbox_categoria.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    String selecionado = (String) checkbox_categoria.getSelectedItem();
                    Categoria categoria = rep_categoria.GetCategoriaPorNome(selecionado, SistemaRepository.sistemaId, Boolean.TRUE);
                    CarregaProdutos(categoria.getCagegoriaId());
                }
            });
    }
    
    public void CarregaCategorias(){
        ArrayList<Categoria> categorias = rep_categoria.GetCategoria(SistemaRepository.sistemaId);
        
        if (categorias.isEmpty()) {
            return;
        }

        for (Categoria categoria : categorias) {
            checkbox_categoria.addItem(categoria.getCategoriaNome());
        }
        
        checkbox_categoria.setSelectedIndex(0);
        
        CarregaProdutos(categorias.get(0).getCagegoriaId());
    }
    
    public void CarregaProdutos(int categoria){
        
        checkbox_produto.removeAllItems();
        
        ArrayList<Produto> produtos = rep_produto.GetProdutosPorCategoria(categoria);
        
        if (produtos.isEmpty()) {
            return;
        }

        for (Produto produto : produtos) {
            checkbox_produto.addItem(produto.getProdutoNome());
        }
        
        checkbox_produto.setSelectedIndex(0);
    }

