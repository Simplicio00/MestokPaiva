    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String categoria_selecionada = checkbox_categoria.getSelectedItem().toString();        
        String produto_selecionado = checkbox_produto.getSelectedItem().toString();
        String inserted_nome = estoque_nome.getText().trim();
        
        float[] valor = new float[1];
        Boolean valido = DecUtils.tryParseFloat(valor_contabil.getText(), valor);
        float valConvertido = valor[0];
        
        /*if (estoque_qtd.getText().isEmpty()) {
            return;
        }*/
        
        if (valido && valConvertido > 0 && valConvertido < 9999999) {
            
            int resposta = JOptionPane.showConfirmDialog(
                null,
                "Deseja realmente cadastrar este estoque para o produto " + produto_selecionado + " ?",
                "Confirmação",
                JOptionPane.OK_CANCEL_OPTION
            );
            
            if (resposta == JOptionPane.CANCEL_OPTION) {
                return;
            }
            
            //vazio
            if (produto_selecionado.isEmpty()) {
                return;
            }

            //menor que 3
            if (inserted_nome.length() < 3) {
                return;
            }

            inserted_nome = StrUtils.RetiraCaracteresEspeciais(inserted_nome);



            Produto produto = rep_produto.GetProdutoPorNome(
                        produto_selecionado, 
                        rep_categoria.GetCategoriaPorNome(categoria_selecionada, SistemaRepository.sistemaId, Boolean.TRUE
            ).getCagegoriaId());

            if (produto != null) {
                rep_estoque.PostEstoqueCategoria(produto.getProdutoId(), inserted_nome, valConvertido);
                PreencheTabelaComEstoqueDeProduto(categoria_selecionada, produto_selecionado);
            }    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

